#!/usr/bin/env node
/**
 * build_data.mjs — Convert cards_scraped.json → js/data.js
 * Run: node build_data.mjs
 */
import fs   from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC  = path.join(__dirname, 'cards_scraped.json');
const DEST = path.join(__dirname, '..', 'js', 'data.js');

const cards = JSON.parse(fs.readFileSync(SRC, 'utf8'));

/* ── Fix image paths from data.js auto-generator ─────── */
// Minor Arcana image names follow: {id}-{Rank}-of-{Suit}.jpg
// but existing files may differ — keep as already set in scrape meta
// Just ensure we don't have "undefined" values
function clean(s) {
  if (!s || s === 'null' || s === 'undefined') return '';
  return s.replace(/\s{2,}/g, ' ').trim();
}

/* ── Serialise one card to compact JS object literal ──── */
function cardToJS(c) {
  const q = (s) => JSON.stringify(clean(s));
  const arr = (a) => '[' + (a||[]).map(q).join(',') + ']';
  const aspect = (k) => {
    const a = c.aspects?.[k] || {};
    return `{up:${q(a.up||'')},rev:${q(a.rev||'')}}`;
  };

  return `  {
    id:${c.id}, name:${q(c.name)}, nameVi:${q(c.nameVi)}, number:${q(c.number)},
    arcana:${q(c.arcana)}, image:${q(c.image)},
    planet:${q(c.planet)}, zodiac:${q(c.zodiac)},
    keywords:${arr(c.keywords)},
    keywordsRev:${arr(c.keywordsRev)},
    upright:${q(c.upright)},
    reversed:${q(c.reversed)},
    aspects:{
      love:${aspect('love')},
      career:${aspect('career')},
      finance:${aspect('finance')},
      health:${aspect('health')},
      spiritual:${aspect('spiritual')}
    },
    advice:${q(c.advice)},
    numerology:${q(c.numerology)}
  }`;
}

const body = cards.map(cardToJS).join(',\n');

const output = `// js/data.js — Full 78-card Tarot database
// Auto-generated by build_data.mjs from tarotreader.me
// Do NOT edit manually — re-run scrape_tarot.mjs + build_data.mjs to update

(function () {
  window.TAROT_DB = [
${body}
  ];

  window.TarotHelper = {
    drawCards(count) {
      const pool = window.TAROT_DB.map(c => ({ ...c, isReversed: Math.random() < 0.3 }));
      for (let i = pool.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [pool[i], pool[j]] = [pool[j], pool[i]];
      }
      return pool.slice(0, count);
    },
    getThemeLabel(v) {
      return { love:'Tình Yêu', career:'Sự Nghiệp', finance:'Tài Chính',
               health:'Sức Khỏe', spiritual:'Tâm Linh', general:'Tổng Quát' }[v] || v;
    },
    getSpreadLabels(n) {
      if (n === 1) return ['Hiện Tại'];
      if (n === 3) return ['Quá Khứ', 'Hiện Tại', 'Tương Lai'];
      if (n === 5) return ['Tình Huống', 'Thách Thức', 'Lời Khuyên', 'Ảnh Hưởng', 'Kết Quả'];
      return Array.from({length:n}, (_,i) => \`Lá \${i+1}\`);
    }
  };
})();
`;

fs.writeFileSync(DEST, output, 'utf8');

// Print stats
const filled = cards.filter(c => c.upright && c.aspects?.love?.up).length;
console.log(`✅ Written → ${DEST}`);
console.log(`   Cards: ${cards.length} | With love.up: ${filled}`);
console.log(`   File size: ${(fs.statSync(DEST).size/1024).toFixed(1)} KB`);
